# Condicional if
# Es establecer un condición cuyo resultado determine como sigue el código

numero = int(input("Ingrese un número entero: "))

if numero > 0:
    print("El número es positivo.")
    print("Hola Positivo!")
else:  
    if numero == 0:
        print("El número es cero")
    else:         
        print("El número es negativo")
        
        
        
        
        
 